// ================== Assignment Operators ==================

// Assignment operators are used to assign values to variables. Here are some common assignment operators in JavaScript:  

// 1. Assignment (=): Assigns a value to a variable.
let x = 10; // Assigns the value 10 to the variable x


// 2. Addition assignment (+=): Adds a value to the variable and assigns the result back to the variable.
x += 5; // Equivalent to x = x + 5; Now x is 15 


// 3. Subtraction assignment (-=): Subtracts a value from the variable and assigns the result back to the variable.
x -= 3; // Equivalent to x = x - 3; Now x is 12


// 4. Multiplication assignment (*=): Multiplies the variable by a value and assigns the result back to the variable.
x *= 2; // Equivalent to x = x * 2; Now x is 24 


// 5. Division assignment (/=): Divides the variable by a value and assigns the result back to the variable.
x /= 4; // Equivalent to x = x / 4; Now x is 6


// 6. Modulus assignment (%=): Takes the modulus of the variable by a value and assigns the result back to the variable.
x %= 5; // Equivalent to x = x % 5; Now x is 1


// In the above examples, we used various assignment operators to modify the value of the variable `x` and logged the results to the console. Assignment operators are essential for updating variable values based on existing values in your code.


