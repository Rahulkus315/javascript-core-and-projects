// ====================== Logical Operators ======================

// Logical operators are used to combine multiple conditions and return a boolean value based on the combined result. Here are some common logical operators in JavaScript: 
// 1. Logical AND (&&): Returns true if both conditions are true, otherwise returns false.
console.log(true && true); // Output: true
console.log(true && false); // Output: false
// 2. Logical OR (||): Returns true if at least one of the conditions is true, otherwise returns false.
console.log(true || false); // Output: true 
console.log(false || false); // Output: false
// 3. Logical NOT (!): Returns true if the condition is false, and false if the condition is true.
console.log(!true); // Output: false
console.log(!false); // Output: true
// In the above examples, we used logical operators to combine conditions and determine the overall truthiness of the expressions. Logical operators are essential for controlling the flow of your program and making decisions based on multiple conditions.
