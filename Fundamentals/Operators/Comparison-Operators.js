// ============================= Comparison Operators =============================

// Comparison operators are used to compare two values and return a boolean (true or false) based on the comparison. Here are some common comparison operators in JavaScript:



// 1. Equal to (==): Checks if two values are equal, performing type coercion if necessary.
console.log(5 == "5"); // Output: true  
// 2. Strict equal to (===): Checks if two values are equal without performing type coercion.
console.log(5 === "5"); // Output: false






// 3. Not equal to (!=): Checks if two values are not equal, performing type coercion if necessary.
console.log(5 != "5"); // Output: false
// 4. Strict not equal to (!==): Checks if two values are not equal without performing type coercion.
console.log(5 !== "5"); // Output: true
// 5. Greater than (>): Checks if the left value is greater than the right value.
console.log(10 > 5);
// 6. Greater than or equal to (>=): Checks if the left value is greater than or equal to the right value.
console.log(10 >= 10);
// 7. Less than (<): Checks if the left value is less than the right value.
console.log(5 < 10);
// 8. Less than or equal to (<=): Checks if the left value is less than or equal to the right value.
console.log(5 <= 5);
// In the above examples, we used various comparison operators to compare values and logged the results to the console. Understanding how these operators work is essential for making decisions in your code and controlling the flow of your program based on conditions.

